package com.ebao.ap99.accounting.model;

public class RevaluationValidModel {
	
	private String inClosingPeriod;
	
	private String executedFlag;
	
	private String currencyCodes;

	public String getInClosingPeriod() {
		return inClosingPeriod;
	}

	public void setInClosingPeriod(String inClosingPeriod) {
		this.inClosingPeriod = inClosingPeriod;
	}

	public String getExecutedFlag() {
		return executedFlag;
	}

	public void setExecutedFlag(String executedFlag) {
		this.executedFlag = executedFlag;
	}

	public String getCurrencyCodes() {
		return currencyCodes;
	}

	public void setCurrencyCodes(String currencyCodes) {
		this.currencyCodes = currencyCodes;
	}
	
	

}
